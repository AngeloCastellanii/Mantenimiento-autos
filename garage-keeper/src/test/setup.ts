import '@testing-library/jest-dom/vitest';

// jsdom no implementa matchMedia (usado por @mantine/hooks useMediaQuery).
if (!window.matchMedia) {
  window.matchMedia = (query: string) => ({
    matches: false,
    media: query,
    onchange: null,
    addListener: () => {},
    removeListener: () => {},
    addEventListener: () => {},
    removeEventListener: () => {},
    dispatchEvent: () => false,
  });
}
